    // ðŸ”¹ Cek tahun kabisat
    private boolean isKabisat(int tahun) {
        return (tahun % 4 == 0 && tahun % 100 != 0) || (tahun % 400 == 0);
    }
    
    // ðŸ”¹ Format hari ke bahasa Indonesia
    private String formatHari(String hari) {
        return switch (hari.toUpperCase()) {
            case "MONDAY" -> "Senin";
            case "TUESDAY" -> "Selasa";
            case "WEDNESDAY" -> "Rabu";
            case "THURSDAY" -> "Kamis";
            case "FRIDAY" -> "Jumat";
            case "SATURDAY" -> "Sabtu";
            case "SUNDAY" -> "Minggu";
            default -> hari;
        };
    }

